def sentence_vector(sentence):

    words = clean_text(sentence)

    valid_words = [word for word in words if word in glove]

    if len(valid_words)==0:

        return np.zeros(200)

    return glove.get_mean_vector(valid_words)

X = np.array(df["Player"].apply(sentence_vector).tolist())

X.shape

def get_sentiment_label(row):
    scores = {'Positive': row['Positive'], 'Neutral': row['Neutral'], 'Negative': row['Negative']}
    max_sentiment = max(scores, key=scores.get)
    return max_sentiment

df['Sentiment'] = df.apply(get_sentiment_label, axis=1)

encoder = LabelEncoder()
